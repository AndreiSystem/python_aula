# + == adição
# - == subtração
# * == multiplicação
# / == divisão
# ** == potência
# // == divisão inteira
# % == resto da divisão

adicao = 5 + 2;
subtracao = 5 - 2;
multiplicacao = 5 * 2;
divisao = 5 / 2;
potencia = 5 ** 2;
divisao_inteira = 5//2;
resto_divisao = 5 % 2;

print(f'5 + 2 = {adicao}');
print(f'5 - 2 = {subtracao}');
print(f'5 * 2 = {multiplicacao}');
print(f'5 / 2 = {divisao}');
print(f'5 ** 2 = {potencia}');
print(f'5 // 2 = {divisao_inteira}');
print(f'5 % 2 = {resto_divisao}');


